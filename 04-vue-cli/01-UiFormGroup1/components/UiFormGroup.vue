<template>
  <div class="form-group" :class="formGroupInline">
    <label v-if="label" class="form-group__label">{{ label }}</label>
    <slot></slot>
  </div>
</template>

<script>
export default {
  name: 'UiFormGroup',

  props: {
    inline: {
      type: Boolean,
      default: false,
    },
    label: {
      type: String,
      require: false,
    },
  },

  computed: {
    formGroupInline() {
      return this.inline ? 'form-group_inline' : false;
    },

    label() {
      return this.label ? this.label : false;
    },
  },
};
</script>

<style scoped>
.form-group {
  position: relative;
  margin-bottom: 24px;
}

.form-group.form-group_inline {
  display: inline-block;
  margin-bottom: 0;
}

.form-group.form-group_inline + .form-group.form-group_inline {
  margin-left: 16px;
}

.form-group__label {
  font-weight: 400;
  font-size: 20px;
  line-height: 28px;
  color: var(--body-color);
  margin-bottom: 10px;
  display: block;
}
</style>
